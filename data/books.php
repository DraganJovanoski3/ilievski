/**
 * Get all categories from books
 */
function getCategories($books) {
    $categories = [];
    foreach ($books as $book) {
        if (!in_array($book['category'], $categories)) {
            $categories[] = $book['category'];
        }
    }
    sort($categories);
    return $categories;
}

/**
 * Get book by ID
 */
function getBookById($books, $id) {
    foreach ($books as $book) {
        if ($book['id'] == $id) {
            return $book;
        }
    }
    return null;
}

/**
 * Get books by category
 */
function getBooksByCategory($books, $category) {
    return array_filter($books, function($book) use ($category) {
        return $book['category'] === $category;
    });
}
